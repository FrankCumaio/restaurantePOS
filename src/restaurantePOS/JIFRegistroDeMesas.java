     public void conectar(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sgrestaurante","root","");
          st=con.createStatement();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
     }
    
    public void listarMesas(){
        String sql= "Select * from mesas";
        try {
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            tableMesas.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            Logger.getLogger(JIFRegistroDeMesas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

