    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      

        
        String nomeMesa = txtNomeMesa.getText();
        int quantLugares = (int) jSpinner2.getValue();
        String descricao = jTextArea1.getText();
       
        DBConnector.salvar(new Mesas(nomeMesa, quantLugares, descricao));
        listarMesas();
        //jSpinner1.setValue(0);
        jSpinner2.setValue(0);
        jTextArea1.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    
    
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       if(tableMesas.getSelectedRow() == -1){
           JOptionPane.showMessageDialog(null,"Seleccione a mesa a ser removida","Atencao",JOptionPane.WARNING_MESSAGE);
       }else{
           DBConnector.apagar(Mesas.class, (int) tableMesas.getValueAt(tableMesas.getSelectedRow(),0));
           listarMesas();
       }
    }//GEN-LAST:event_jButton4ActionPerformed

